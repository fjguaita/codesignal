import numpy as np
def sudoku2(grid):
    grid=np.array(grid)
    for i in range(9):
        fila=[n for n in grid[i,:] if n!='.']
        columna=[n for n in grid[:,i] if n!='.']
        if len(fila)!=len(set(fila)) or len(columna)!=len(set(columna)):
            return False
    
    for i in range(0,9,3):
        for j in range(0,9,3):
            aux=[n for n in grid[i:i+3,j:j+3].reshape(1,9)[0] if n!='.']
            if len(aux)!=len(set(aux)):
                return False       
    return True


